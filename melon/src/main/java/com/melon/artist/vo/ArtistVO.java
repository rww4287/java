package com.melon.artist.vo;

public class ArtistVO {

	private String artisId;
	private String member;
	private String debutDate;
	private String debutTitle;
	
	public String getArtisId() {
		return artisId;
	}
	public void setArtisId(String artisId) {
		this.artisId = artisId;
	}
	public String getMember() {
		return member;
	}
	public void setMember(String member) {
		this.member = member;
	}
	public String getDebutDate() {
		return debutDate;
	}
	public void setDebutDate(String debutDate) {
		this.debutDate = debutDate;
	}
	public String getDebutTitle() {
		return debutTitle;
	}
	public void setDebutTitle(String debutTitle) {
		this.debutTitle = debutTitle;
	}
	
}
